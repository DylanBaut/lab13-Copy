; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; The program below prints out a series of several 16 by 8 character images
; of an preselected String made up of ASCII characters which has darkened 
; space and white space made up of two other preselected ASCII characters. 
; The program first iterates by 16 rows, each time setting R2 to the starting
; mapped letter. The inner loop of the letter will set R6 to the content at the 
; incremented R2 value, which is then multiplied by 16 and added the row count. This value
; is then added to the FONT_DATA starting address, then the 8 counter for columns is set.
; The program traverses the content at the row address of the ASCII character by seeing 
; if its negative or not and applying the applicable character. The correct character is
; printed by loading R1 with either the value at BLANK or CHAR. The sequence is then reset by 
; shifting left, or the letter ends and the program moves onto the next. Once the ASCII Hex 
; value is identified to be NULL, the letter string finishes by incrementing the row counter 
; and printing a newline character. The program ends when all 16 rows are filled.

;R1 Blank (0) Address or Character (1) Address
;R2 Current Mapped letter address
;R3 Row counter
;R4 Column counter
;R5 Value at Row Address (RSRc) 
;R6 Mapped character (RSR)/Row location

.ORIG x3000
			LD R1, BLANK 			; R1 gets value at BLANK (x5000)
			AND R3, R3, #0			; Clear R3
			
			
NEXT_ROW	ADD R3, R3, #0			; test if row counter is 16 
			ADD R3, R3, #-16		; Row counter is 16 if the value minus 16 is 0
			BRz DONE				; if is, done
			ADD R3, R3, #6			; Add 16 back to R3
			ADD R3, R3, #10
			LD R2, MAP				; R2 gets value at MAP (x5002)
			ADD R2, R2, #-1			; Subtract 1 to account for first increment

NEXT_LET	AND R6, R6, #0			; Clear R6
			ADD R2, R2, #1			; Increment letter location
			ADD R6, R2, #0			; R6 gets next letter
			LDR R6, R6, #0			; R6 gets loaded the content of its own address (ASCII HEX)
			BRz	DONE_LETTS			; If the value is null (x0000), letter string done
 						
			AND R4, R4, #0			; Clear R4
			
			ADD R6, R6, R6			; Multiply R6 by 16
			ADD R6, R6, R6
			ADD R6, R6, R6
			ADD R6, R6, R6

			LEA R4, FONT_DATA		; ADD R6 to start of FONT DATA
			ADD R6, R4, R6
			AND R4, R4, #0			; Clear R4
			
			ADD R6, R3, R6			; Row count gets added to R6 (moving to next row)

			AND R4, R4, #0			; col counter 0
			ADD R4, R4, #8			; col counter #8
			
			LDR R5, R6, #0			; Load the content of the address of the row

NEXT_COL	ADD R5, R5, #0			; Test if the character is a 1 or 0
			BRn ADD_CHAR			; If it is a 1, print Character
			BR ADD_BLANK			; If it is a 0, print Blank

ADD_CHAR	AND R1, R1, #0			; Clear R1
			LD R1, CHAR				; R1 gets value at CHAR (x5001)
			LDR R0, R1, #0			; Print a 1 character
			OUT
			LD R1, BLANK 			; R1 gets value at BLANK (x5000)
			ADD R4, R4, #-1			; Decrement Col count
			ADD R4, R4, #0			; test if col counter is 0
			BRz NEXT_LET			; if is, done row
			ADD R5, R5, R5			; If not,shift left and move to next COL
			BR NEXT_COL

ADD_BLANK	LDR R0, R1, #0			; Print a 0 character
			OUT		
			ADD R4, R4, #-1			; Decrement Col count
			ADD R4, R4, #0			; test if col counter is 0
			BRz NEXT_LET			; if is, done row
			ADD R5, R5, R5	
			BR NEXT_COL				; If not,shift left and move to next COL

DONE_LETTS	ADD R3, R3, #1			; Increment Row count
			LD R0, NEWLINE			; Print a New Line
			OUT
			BR NEXT_ROW				; Move to Next Row
		

DONE		TRAP x25				; Halt

BLANK		.FILL x5000
CHAR		.FILL x5001
MAP			.FILL x5002
NEWLINE 	.FILL x0A

